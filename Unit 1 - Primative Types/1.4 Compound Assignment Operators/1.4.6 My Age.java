public class MyAge
{
    public static void main(String[] args)
    {
        // make my age and print it
        int age = 14;
        System.out.println("My current age is: " + age);
        // next year age
        age++;
        System.out.println("My age next year will be: " + age);
        // go back
        age--;
        System.out.println("My current age is: " + age);
    }
}